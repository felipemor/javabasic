public class maiornumero {

    public static void main(String[] args) {
        var x = 4;
        var y = 5;

        if (x > y) {
            System.out.println(x);
        } else if (y > x) {
            System.out.println("Maior número é" + y);
        } else {
            System.out.println("Invalido");
        }
    }
}
